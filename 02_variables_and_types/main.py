# Basic Variables and Data Types
# This file shows fundamental variable types in Python

# Example 1: Numeric Types
print("Example 1 - Numeric Types:")
number = 42          # Integer
pi = 3.14           # Float
print(f"Integer: {number}, Type: {type(number)}")
print(f"Float: {pi}, Type: {type(pi)}")

# Example 2: String and Boolean
print("\nExample 2 - String and Boolean:")
name = "Python"     # String
is_fun = True       # Boolean
print(f"String: {name}, Type: {type(name)}")
print(f"Boolean: {is_fun}, Type: {type(is_fun)}")

# Variables and Basic Data Types
# This file demonstrates different types of variables and data types in Python

# Numeric types
integer_number = 42
float_number = 3.14
complex_number = 1 + 2j

print("Numeric Types:")
print(f"Integer: {integer_number}, Type: {type(integer_number)}")
print(f"Float: {float_number}, Type: {type(float_number)}")
print(f"Complex: {complex_number}, Type: {type(complex_number)}")

# Boolean type
is_python_fun = True
is_java_better = False

print("\nBoolean Types:")
print(f"Python is fun: {is_python_fun}, Type: {type(is_python_fun)}")
print(f"Java is better: {is_java_better}, Type: {type(is_java_better)}")

# String type
first_name = "John"
last_name = 'Doe'
multi_line = """This is a
multi-line string"""

print("\nString Types:")
print(f"First Name: {first_name}, Type: {type(first_name)}")
print(f"Last Name: {last_name}, Type: {type(last_name)}")
print(f"Multi-line: {multi_line}")

# None type
empty_value = None
print(f"\nNone Type: {empty_value}, Type: {type(empty_value)}")

