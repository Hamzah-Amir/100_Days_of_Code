# Basic String Operations and Slicing
# This file shows fundamental string operations in Python

# Example 1: Basic String Operations
text = "Hello Python"
print("Example 1 - Basic Operations:")
print("Original text:", text)
print("Uppercase:", text.upper())
print("Lowercase:", text.lower())
print("Length:", len(text))

# Example 2: String Slicing
print("\nExample 2 - String Slicing:")
print("First 5 characters:", text[:5])
print("Last 6 characters:", text[-6:])
print("Characters from index 6 to 8:", text[6:9]) 